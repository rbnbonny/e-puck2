static void serial_start(void)
{
	static SerialConfig ser_cfg = {
	    115200,
	    0,
	    0,
	    0,
	};

	sdStart(&SD3, &ser_cfg); // UART3.
}

int main(void)
{

    halInit();
    chSysInit();
    mpu_init();

    //starts the serial communication
    serial_start();
    //starts the USB communication
    usb_start();
    //starts the camera
    dcmi_start();
	po8030_start();

    process_image_start();

    while(1){
    	chprintf((BaseSequentialStream *)&SDU1, "Test\r\n");
    }
}

